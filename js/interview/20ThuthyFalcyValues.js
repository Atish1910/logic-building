// In JavaScript, values are categorized as truthy or falsy when evaluated in a Boolean context (such as an if condition).

// Falsy Values
// A falsy value is one that is treated as false when converted to a Boolean. The following values are falsy:

// false – Boolean false
// 0 – Number zero
// -0 – Negative zero
// 0n – BigInt zero
// "" – Empty string
// null – Absence of value
// undefined – Variable declared but not assigned a value
// NaN – Not a number




// Truthy Values
// A truthy value is anything that is not falsy. Some common examples include:

// true
// Non-zero numbers (5, -3, 0.1, etc.)
// Non-empty strings ("hello", "false", "0")
// Objects ({}, [])
// Functions (function() {})
// Symbols (Symbol())
// BigInt values other than 0n (1n, -2n)